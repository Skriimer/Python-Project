# 1. Площадь квадрата со стороной 15
side_square = 15
area_square = side_square ** 2
print("Площадь квадрата:", area_square)

# 2. Периметр прямоугольника со сторонами 10 и 25
side_a = 10
side_b = 25
perimeter_rectangle = 2 * (side_a + side_b)
print("Периметр прямоугольника:", perimeter_rectangle)

# 3. Объем куба с ребром 7
edge_cube = 7
volume_cube = edge_cube ** 3
print("Объем куба:", volume_cube)
