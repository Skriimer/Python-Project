# Ввод строки от пользователя
print("Введите слово для проверки:",end=" ")
word = input()
# word = input("Введите слово для проверки: ")

# Приводим к нижнему регистру для игнорирования регистра
word_lower = word.lower()

# Переворачиваем строку
reversed_word = word_lower[::-1]

# Проверяем, является ли строка палиндромом
if word_lower == reversed_word:
    print(f"\n'{word}' - это палиндром!")
else:
    print(f"\n'{word}' - это НЕ палиндром!")

# Дополнительная информация для наглядности
print(f"Исходное слово: {word}")
print(f"Перевернутое: {word[::-1]}")